# Sum
print(2 + 2)  # 4

# Rest
print(4 - 2)  # 2

# Multiply
print(4 * 2)  # 8

# Division
print(4 / 2)  # 2

print(2 // 4)  # No decimal

# Pow
print(2**3)  # 2 * 2 * 2

# Module (division residual)
print(6 % 4)  # 2

# Round
print(round(3.1))
print(round(3.9))

# Absolute value
print(abs(20))
print(abs(-20))
