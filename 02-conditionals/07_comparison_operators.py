# Equal
print(5 == 5)  # True
print(5 == 3)  # False
print("Hello" == "Hello")  # True

# Inequity
print(5 != 5)  # False
print(5 != 3)  # True
print("Hello" != "Hello")  # False

# Greater than
print(7 > 5)  # True
print(5 > 9)  # False

# Less than
print(7 < 5)  # False
print(5 < 9)  # True

# Greater than or equal to
print(7 >= 5)  # True
print(5 >= 9)  # False

# Less than or equal to
print(7 <= 5)  # False
print(5 <= 9)  # True
